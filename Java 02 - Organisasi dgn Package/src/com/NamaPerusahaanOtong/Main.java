package com.NamaPerusahaanOtong;

public class Main{  // body si program java
    
    public static void main(String[] args){
        System.out.println("hello world");
    }
    
}

/*
Cara Ekeskusi program dengan Terminal

1. Masuk ke directory pada folder src untuk meng compile file program Main.java
2. ketik: javac com/NamaPerusahaanOtong/Main.java -d ../bin
3. akan muncul folder baru dengan nama com dan dalam folder com ada folder NamaPerusahaanOtong
4. dalam folder NamaPerusahaanOtong ada file Main.class hasil dari compile an si Main.java
5. untuk eksekusi filenya, masuk ke directory bin pada terminal
6. ketik: java com.NamaPerusahaanOtong.Main
7. Hasilnya akan muncul

*/